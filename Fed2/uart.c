/******************************************************************************
* Function:         void uart0_init(uint16_t baud)
* Description:      UART 0 inicializalasa
* Input:
* Output:
* Notes:
******************************************************************************/
void uart0_init(uint16_t baud)
{
	/* Set baud rate */
	UBRR0H = (unsigned char) (baud>>8);
	UBRR0L = (unsigned char) baud;
	/* Set frame format: 8data, no parity & 1 stop bits */
	UCSR0C = (0<<UMSEL0) | (0<<UPM0) | (1<<USBS0) | (1<<UCSZ0) | (1<<UCSZ1);
	/* Enable receiver and transmitter */
	UCSR0B = (1<<RXEN0) | (1<<TXEN0) | (0<<RXCIE0);
	
}

/******************************************************************************
* Function:         void uart_write(uint8_t data)
* Description:      adat kuldese uarton
* Input:
* Output:
* Notes:
******************************************************************************/
void uart_write(uint8_t data)
{
	while (!(UCSR0A & (1 << UDRE0)));
	UDR0 = data;
}

